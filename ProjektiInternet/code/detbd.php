            <h1 class="titulli">Detyra 1- Krijimi i një databaze për menaxhimin e bibliotekës</h1>
            <h2>Organizimi i projektit</h2>
            <p>Projekti është i ndarë në 4 faza. Udhëzimet për secilën fazë do të jepen
menjëherë me të mbaruar të fazës paraprake. Qëllimet e projektit janë:
 Të implementoni një databazë të vogël relacionale, megjithatë
komplete.
 Të respektoni problemet e botës reale me të cilat ballafaqohen
njerëzit kur ndajnë të dhëna.
Për të implementuar projektin (databazën tuaj), do të keni në dispozicion
njërin prej sistemeve të ças’shme falas për menaxhim të bazave të të
dhënave.
</p>
            <h2>Qëllimi i projektit</h2>
            <p>Qëllimi i këtij projekti është të krijoni një skemë, një databazë relacionale
në njërin prej sistemeve të ças’shme falas për menaxhim të bazës së të
dhënave. Pritet të krijoni një skemë, një databazë relacionale, dhe të
përkrahni disa pyetësorë (query).
Në secilin prej projekteve, do të merrni udhëzimet mbi atë se çfarë të ruani
në databazën relacionale. Përshkrimi i projekteve është jo formal, dhe ju
duhet të krijoni shprehjet përkatëse në algjebrën relacionale (faza 1A), në
modelin logjik (faza 1B), dhe në SQL (faza 3).
Për të dizajnuar skemën tuaj, ju duhet të udhëhiqeni me dizajnin
konceptual, dm.th., të krijoni diagramet ER përkatëse (faza 2). Së fundi,
duhet të aplikoni njohuritë e fituara nga lënda në lidhje me dizajnin formal
të një databaze, d.m.th, normalizimin, në mënyrë që databaza juaj jo
vetëm të zgjedh problemin e parashtruar me këtë projekt, por ta zgjedh
atë në mënyrë sa më optimale (faza 4).
</p>
<h2>Të dhënat</h2>
<p>Sa për fillim, popullojeni databazën tuaj relacionale me disa të dhëna
shembuj. Databaza duhet të përmbajë të paktën 10 rreshta me të dhëna
për tabelë, dhe të demonstroj sa më shumë aspekte të skemës suaj që
është e mundur. Mundohuni të jeni sa më kreativ.
</p>
<h2>Plani i Projektit</h2>
<p>Skema juaj relacionale dhe instanca shembuj (të dhëna) për çdo
relacion; Shprehjet e algjebrës relacionale për querit e dhënë;
Shprehjet e logjikës Datalog për querit e dhënë; Implementoni databazën në SQL – të keni parasysh të gjitha
kushtëzimet e integritetit (IC-të); SQL query-it (1-9); 
Krijoni se paku dy view, dy trigger, dy stored procedure dhe së paku
një funksion; Të dizajnohet një diagram ER i cili i plotëson të gjitha kriteret e dhëna
në përshkrimin e projektit; 
Normalizoni bazën e të dhënave në mënyrë që jo vetëm të zgjedh
problemin e parashtruar me këtë projekt, por ta zgjedh atë në
mënyrë sa më optimale; </p>
