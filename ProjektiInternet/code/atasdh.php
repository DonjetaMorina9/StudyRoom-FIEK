            <h1 class="titulli">Detyra 1- Krijimi i klasës Temperatura</h1>
            <p>Të krijohet një program në gjuhën programuese C++ i cili përmban klasën Temperatura, si dhe funksionin
main sipas përshkrimit dhe kërkesave në vijim:
<ul>
    <li>Në pjesën private, klasa Temperatura duhet të përmbaj një listë të lidhur për numra të plotë
që deklarohet prej strukturës së të dhënave 'list' nga libraria STL (Standard Template Library) dhe
e kam emrin L</li>
<li>Në pjesën publike, klasa Temperatura duhet të përmbaj funksionet si në vijim si në vijim:</li>
<ul>
    <li>Funksioni Mbushja që mundëson mbushjen e listës L me 7 numra të cilët
përdoruesi i jep përmes tastierës. Numrat që futën në listë mund të jenë vetëm prej vlerës
5 deri në 20, andaj nëse përdoruesi jep një numër jashtë këtij diapazoni, duhet të paraqitet
mesazhi përkatës për futje të serishme të vlerës</li>
<li>Funksioni Shtypja që mundëson shtypjen e numrave që gjendën në listën L</li>
<li>Funksioni Numerimi që mundëson numërimin numrave dyshifror që gjendën në
listën L,</li>
<li>Funksioni Zgjerimi që zgjeron listën L edhe me dy anëtar shtesë, ku anëtari i
parë paraqet shumën e anëtarëve të deritanishëm në listë, ndërsa anëtari i dytë parqet
prodhimin e anëtarëve të deritanishëm. Anëtari i shumës le të shtohet në fillim të listës,
ndërsa anëtari i prodhimit le të shtohet në fund të listës</li>
</ul>
<li>Funksionin main, ku krijohet një objekt nga klasa temparatura dhe thirrën të gjitha
funksionet e klasës për t’i shtypur rezultatet e llogaritura.</li>
</ul>
</p>
